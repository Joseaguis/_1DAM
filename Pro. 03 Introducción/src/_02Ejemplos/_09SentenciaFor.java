package _02Ejemplos;

import java.util.Scanner;

public class _09SentenciaFor {
	public static void main(String[] args) {
		// Imprimir por pantalla del 1 al 10
		
		int a = 1;
		while (a <= 10) {
			System.out.println(a);
			a++;
		}
		System.out.println("Con For");
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		
	}
}
