package _02Ejemplos.Alumnos;

public class Alumno extends Persona{
	private int nia;
	private String grupo;
	
	
	
	public int getNia() {
		return nia;
	}
	public String getGrupo() {
		return grupo;
	}
	public void setNia(int nia) {
		this.nia = nia;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	
	
}
